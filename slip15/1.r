# Q1.Write a R program to add, multiply and divide two vectors of integer type.(vector length
# should be minimum 4)

# Create two integer vectors
vector1 <- c(10, 20, 30, 40)
vector2 <- c(1, 2, 3, 4)

# Addition of the two vectors
addition <- vector1 + vector2
cat("Addition of vectors:\n")
print(addition)

# Multiplication of the two vectors
multiplication <- vector1 * vector2
cat("Multiplication of vectors:\n")
print(multiplication)

# Division of the two vectors
division <- vector1 / vector2
cat("Division of vectors:\n")
print(division)
