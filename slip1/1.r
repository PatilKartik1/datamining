# Q1.Write a R program to add, multiply and divide two vectors of integertype. (Vector
# length should be minimum 4)

# Define two vectors of integer type
vector1 <- c(10, 20, 30, 40)
vector2 <- c(2, 4, 6, 8)

# Addition
add_result <- vector1 + vector2
print("Addition of the two vectors:")
print(add_result)

# Multiplication
multiply_result <- vector1 * vector2
print("Multiplication of the two vectors:")
print(multiply_result)

# Division
divide_result <- vector1 / vector2
print("Division of the two vectors:")
print(divide_result)
