# R program to find the maximum and minimum value of a vector

# Define a vector
vector <- c(34, 7, 23, 32, 5, 62, 9, 1, 57, 12)

# Find the maximum value
max_value <- max(vector)
print(paste("The maximum value in the vector is:", max_value))

# Find the minimum value
min_value <- min(vector)
print(paste("The minimum value in the vector is:", min_value))
